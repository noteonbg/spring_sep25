package a00anotherpoc.model;

// PA.java (Perimeter-Area response object)
public class PA {
    private double area;
    private double perimeter;

    public PA(double area, double perimeter) {
        this.area = area;
        this.perimeter = perimeter;
    }

    // Getters
    public double getArea() { return area; }
    public double getPerimeter() { return perimeter; }
}

